// == (comparação implícita) - só compara o valor

let numero = 1;
let texto = "1";

console.log(numero == texto);

// === (comparação explícita) - compara o valor e o tipo de dado (boa prática)

let numero2 = 1;
let texto2 = "1";

console.log(numero2 === texto2);

//typeof
console.log(typeof numero);
console.log(typeof texto);

